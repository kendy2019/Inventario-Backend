package com.inventario.dto;

public class CotizacionDTO {
    private String clienteNombre;
    private String clienteTelefono;
    private String marcaCelular;
    private String detalleCelular;
    private String observaciones;
	public String getClienteNombre() {
		return clienteNombre;
	}
	public void setClienteNombre(String clienteNombre) {
		this.clienteNombre = clienteNombre;
	}
	public String getClienteTelefono() {
		return clienteTelefono;
	}
	public void setClienteTelefono(String clienteTelefono) {
		this.clienteTelefono = clienteTelefono;
	}
	public String getMarcaCelular() {
		return marcaCelular;
	}
	public void setMarcaCelular(String marcaCelular) {
		this.marcaCelular = marcaCelular;
	}
	public String getDetalleCelular() {
		return detalleCelular;
	}
	public void setDetalleCelular(String detalleCelular) {
		this.detalleCelular = detalleCelular;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}


    
    
    
}